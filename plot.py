def draw_plot(name, data, dataType):
    fold0_result = data[0]
    fold1_result = data[1]
    fold2_result = data[2]
    fold3_result = data[3]
    fold4_result = data[4]
    iterations = [i for i in range(len(fold4_result))]

    plt.title(f'{dataType}: {name} trends')
    xlocs = [i for i in range(len(fold4_result))]
    plt.xticks(xlocs, iterations)

    plt.plot(fold0_result, 'r-o')
    plt.plot(fold1_result, 'b-x')
    plt.plot(fold2_result, 'g-^')
    plt.plot(fold3_result, 'c-v')
    plt.plot(fold4_result, 'k-*')

    plt.xlabel('Iteration')
    plt.ylabel(f'{name}')

    varNames = ['Fold0', 'Fold1', 'Fold2', 'Fold3', 'Fold4']
    plt.legend(varNames, loc='best')

    plt.grid(axis='both')
    plt.show()


